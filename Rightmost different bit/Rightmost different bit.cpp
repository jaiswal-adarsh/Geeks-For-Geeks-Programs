class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here  
       int t=m^n;
       if(t==0)return -1;
        int cnt=1;
        while(t%2==0 && t>0)
        {
            cnt++;
            t=t>>1;
        }
        return cnt;
    }
};

